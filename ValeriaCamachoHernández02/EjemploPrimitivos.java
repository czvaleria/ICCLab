public class EjemploPrimitivos {
public static void main(String[] args) {

//Tipo numérico:
int unInt = 771;
float unFloat = 567.89f;
double unDouble = 3.4523421;

//Tipo carácter:
char unChar = 'M';
String unString = "¡Hola!";

//Tipo booleano: 
boolean unBool = true;

//En terminal
System.out.println("El valor de un tipo de variable int es: "+unInt);
System.out.println("El valor de un tipo de variable float es: "+unFloat);
System.out.println("El valor de un tipo de variable double es: "+unDouble);
System.out.println("El valor de un tipo de variable char es: "+unChar);
System.out.println("El valor de una String es: "+unString);
System.out.println("El valor de un tipo de variable boolean es: "+unBool);

  }
}
